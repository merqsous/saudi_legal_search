@router.get("/legal-study/{study_id}/export/docx")
def export_docx(study_id: int, authorization: str = Header(None), token: str = Query(None)):
    """Export a legal study as a Word document with Albaheth branding."""
    user_id = _get_user_from_auth(authorization, token)
    if not user_id:
        raise HTTPException(status_code=401, detail="غير مصرح")

    study = _get_study(study_id, user_id)

    try:
        from docx import Document
        from docx.shared import Pt, Inches, RGBColor, Cm
        from docx.enum.text import WD_ALIGN_PARAGRAPH
        from docx.oxml.ns import qn
    except ImportError:
        raise HTTPException(status_code=500, detail="python-docx not installed")

    doc = Document()

    style = doc.styles['Normal']
    font = style.font
    font.name = 'Arial'
    font.size = Pt(12)
    # Set Normal style to RTL
    style_pPr = style.element.get_or_add_pPr()
    style_bidi = style_pPr.makeelement(qn('w:bidi'), {})
    style_pPr.append(style_bidi)

    for section in doc.sections:
        section.left_margin = Cm(2.5)
        section.right_margin = Cm(2.5)
        section.top_margin = Cm(2.5)
        section.bottom_margin = Cm(2.5)
        # Set section to RTL
        sectPr = section._sectPr
        bidi = sectPr.makeelement(qn('w:bidi'), {})
        sectPr.append(bidi)

    def _set_rtl(paragraph):
        """Set paragraph to RTL."""
        pPr = paragraph._p.get_or_add_pPr()
        bidi = pPr.makeelement(qn('w:bidi'), {})
        pPr.append(bidi)

    def _set_run_rtl(run):
        """Set run to RTL."""
        rPr = run._r.get_or_add_rPr()
        rtl = rPr.makeelement(qn('w:rtl'), {})
        rPr.append(rtl)

    def _add_arabic_run(paragraph, text, size=12, color=None, bold=False, italic=False):
        """Add a run with RTL flag and proper Arabic font."""
        run = paragraph.add_run(text)
        run.font.size = Pt(size)
        if color:
            run.font.color.rgb = color
        run.bold = bold
        run.italic = italic
        _set_run_rtl(run)
        return run

    GREEN = RGBColor(0x0B, 0x4B, 0x2A)
    GREEN_LIGHT = RGBColor(0x2E, 0x7D, 0x4F)
    MUTED = RGBColor(0x65, 0x75, 0x6B)

    # Header
    header = doc.sections[0].header
    header_para = header.paragraphs[0]
    header_para.alignment = WD_ALIGN_PARAGRAPH.CENTER
    _add_arabic_run(header_para, "الباحث — Albaheth", size=14, color=GREEN, bold=True)

    # Title
    title = doc.add_paragraph()
    title.alignment = WD_ALIGN_PARAGRAPH.CENTER
    _set_rtl(title)
    _add_arabic_run(title, "دراسة قانونية", size=22, color=GREEN, bold=True)

    # Subtitle
    subtitle = doc.add_paragraph()
    subtitle.alignment = WD_ALIGN_PARAGRAPH.CENTER
    _set_rtl(subtitle)
    _add_arabic_run(subtitle, f"الموضوع: {study['query']}", size=14, color=GREEN_LIGHT, bold=True)

    # Date
    date_para = doc.add_paragraph()
    date_para.alignment = WD_ALIGN_PARAGRAPH.CENTER
    _set_rtl(date_para)
    created = study.get("created_at", "")
    if hasattr(created, "strftime"):
        date_str = created.strftime("%Y-%m-%d %H:%M")
    else:
        date_str = str(created)
    _add_arabic_run(date_para, f"التاريخ: {date_str}", size=10, color=MUTED)

    doc.add_paragraph()

    # Content
    content = study["content"]
    for line in content.split("\n"):
        line = line.strip()
        if not line:
            continue

        p = doc.add_paragraph()
        _set_rtl(p)

        if _is_heading(line):
            p.alignment = WD_ALIGN_PARAGRAPH.LEFT
            _add_arabic_run(p, _strip_markdown(line), size=14, color=GREEN, bold=True)
        elif _is_numbered_heading(line):
            p.alignment = WD_ALIGN_PARAGRAPH.LEFT
            clean = re.sub(r'^\d+\.\s+', '', line)
            parts = re.split(r'(\*\*.+?\*\*)', clean)
            for part in parts:
                if part.startswith('**') and part.endswith('**'):
                    _add_arabic_run(p, _strip_markdown(part), size=13, color=GREEN, bold=True)
                else:
                    _add_arabic_run(p, part, size=13, color=GREEN, bold=True)
        elif _is_bold_line(line):
            p.alignment = WD_ALIGN_PARAGRAPH.LEFT
            _add_arabic_run(p, _strip_markdown(line), size=12, color=GREEN_LIGHT, bold=True)
        else:
            p.alignment = WD_ALIGN_PARAGRAPH.LEFT
            parts = re.split(r'(\*\*.+?\*\*)', line)
            for part in parts:
                if part.startswith('**') and part.endswith('**'):
                    _add_arabic_run(p, _strip_markdown(part), size=12, bold=True)
                else:
                    _add_arabic_run(p, part, size=12)

    # Citations
    doc.add_paragraph()
    citations_heading = doc.add_paragraph()
    _set_rtl(citations_heading)
    citations_heading.alignment = WD_ALIGN_PARAGRAPH.LEFT
    _add_arabic_run(citations_heading, "الأحكام المرجعية", size=16, color=GREEN, bold=True)

    for i, cite in enumerate(study.get("citations", [])):
        p = doc.add_paragraph()
        _set_rtl(p)
        p.alignment = WD_ALIGN_PARAGRAPH.LEFT
        _add_arabic_run(p, _format_citation(i, cite), size=10, color=MUTED)

    # Disclaimer
    doc.add_paragraph()
    disclaimer_heading = doc.add_paragraph()
    _set_rtl(disclaimer_heading)
    disclaimer_heading.alignment = WD_ALIGN_PARAGRAPH.LEFT
    _add_arabic_run(disclaimer_heading, "إخلاء مسؤولية", size=12, color=GREEN, bold=True)

    p = doc.add_paragraph()
    _set_rtl(p)
    p.alignment = WD_ALIGN_PARAGRAPH.LEFT
    _add_arabic_run(p, DISCLAIMER_AR, size=10, color=MUTED, italic=True)

    p = doc.add_paragraph()
    p.alignment = WD_ALIGN_PARAGRAPH.CENTER
    _add_arabic_run(p, DISCLAIMER_EN, size=9, color=MUTED, italic=True)

    # Footer
    footer = doc.sections[0].footer
    footer_para = footer.paragraphs[0]
    footer_para.alignment = WD_ALIGN_PARAGRAPH.CENTER
    run = footer_para.add_run("Albaheth — AI-Powered Legal Research | albaheth.app")
    run.font.size = Pt(8)
    run.font.color.rgb = GREEN

    buffer = io.BytesIO()
    doc.save(buffer)
    buffer.seek(0)

    filename = f"legal_study_{study_id}.docx"
    return StreamingResponse(
        buffer,
        media_type="application/vnd.openxmlformats-officedocument.wordprocessingml.document",
        headers={"Content-Disposition": f'attachment; filename="{filename}"'},
    )


