# PDF Export — uses WeasyPrint (HTML to PDF) with native Arabic RTL support
# ============================================================
@router.get("/legal-study/{study_id}/export/pdf")
def export_pdf(study_id: int, authorization: str = Header(None), token: str = Query(None)):
    """Export a legal study as a PDF with Albaheth branding."""
    user_id = _get_user_from_auth(authorization, token)
    if not user_id:
        raise HTTPException(status_code=401, detail="غير مصرح")

    study = _get_study(study_id, user_id)

    try:
        from weasyprint import HTML
    except ImportError:
        raise HTTPException(status_code=500, detail="weasyprint not installed")

    created = study.get("created_at", "")
    if hasattr(created, "strftime"):
        date_str = created.strftime("%Y-%m-%d %H:%M")
    else:
        date_str = str(created)

    # Build HTML content — same format as webpage
    content = study["content"]
    body_parts = []
    for line in content.split("\n"):
        line = line.strip()
        if not line:
            continue
        if _is_heading(line):
            body_parts.append(f'<h3 style="color:#0B4B2A;font-size:14pt;font-weight:bold;margin-top:12px;margin-bottom:6px;">{_strip_markdown(line)}</h3>')
        elif _is_numbered_heading(line):
            clean = _strip_markdown(line)
            body_parts.append(f'<h3 style="color:#0B4B2A;font-size:13pt;font-weight:bold;margin-top:12px;margin-bottom:6px;">{clean}</h3>')
        elif _is_bold_line(line):
            body_parts.append(f'<p style="color:#2E7D4F;font-size:12pt;font-weight:bold;margin:4px 0;">{_strip_markdown(line)}</p>')
        else:
            # Handle inline bold
            html_line = re.sub(r'\*\*(.+?)\*\*', r'<strong>\1</strong>', line)
            body_parts.append(f'<p style="font-size:12pt;line-height:1.6;margin-bottom:8px;text-align:justify;">{html_line}</p>')

    body_html = "\n".join(body_parts)

    # Citations HTML
    citations_html = ""
    for i, cite in enumerate(study.get("citations", [])):
        text = _format_citation(i, cite)
        citations_html += f'<p style="font-size:10pt;color:#65756B;margin-bottom:4px;padding-right:20px;">{text}</p>\n'

    # Full HTML document with RTL and Arabic font
    html = f"""<!DOCTYPE html>
<html lang="ar" dir="rtl">
<head>
<meta charset="utf-8">
<style>
  @page {{
    size: A4;
    margin: 2.5cm;
    @bottom-center {{
      content: "Albaheth — AI-Powered Legal Research | albaheth.app";
      font-size: 8pt;
      color: #0B4B2A;
    }}
  }}
  body {{
    direction: rtl;
    font-family: "Arial", "Helvetica", sans-serif;
    font-size: 12pt;
    color: #333;
  }}
  .header {{
    text-align: center;
    border-bottom: 2px solid #0B4B2A;
    padding-bottom: 10px;
    margin-bottom: 20px;
  }}
  .header h1 {{
    color: #0B4B2A;
    font-size: 14pt;
    margin: 0;
  }}
  .title {{
    text-align: center;
    font-size: 22pt;
    color: #0B4B2A;
    font-weight: bold;
    margin-bottom: 4px;
  }}
  .subtitle {{
    text-align: center;
    font-size: 14pt;
    color: #2E7D4F;
    font-weight: bold;
    margin-bottom: 4px;
  }}
  .date {{
    text-align: center;
    font-size: 10pt;
    color: #65756B;
    margin-bottom: 20px;
  }}
  .citations-heading {{
    color: #0B4B2A;
    font-size: 16pt;
    font-weight: bold;
    margin-top: 20px;
    margin-bottom: 8px;
  }}
  .disclaimer-heading {{
    color: #0B4B2A;
    font-size: 12pt;
    font-weight: bold;
    margin-top: 20px;
    margin-bottom: 6px;
  }}
  .disclaimer-ar {{
    font-size: 10pt;
    color: #65756B;
    font-style: italic;
    text-align: justify;
    margin-bottom: 4px;
  }}
  .disclaimer-en {{
    font-size: 9pt;
    color: #65756B;
    font-style: italic;
    text-align: center;
  }}
</style>
</head>
<body>
  <div class="header">
    <h1>الباحث — Albaheth</h1>
  </div>
  <div class="title">دراسة قانونية</div>
  <div class="subtitle">الموضوع: {study['query']}</div>
  <div class="date">التاريخ: {date_str}</div>

  {body_html}

  <div class="citations-heading">الأحكام المرجعية</div>
  {citations_html}

  <div class="disclaimer-heading">إخلاء مسؤولية</div>
  <p class="disclaimer-ar">{DISCLAIMER_AR}</p>
  <p class="disclaimer-en">{DISCLAIMER_EN}</p>
</body>
</html>"""

    buffer = io.BytesIO()
    HTML(string=html).write_pdf(buffer)
    buffer.seek(0)

    filename = f"legal_study_{study_id}.pdf"
    return StreamingResponse(
        buffer,
        media_type="application/pdf",
        headers={"Content-Disposition": f'attachment; filename="{filename}"'},
    )
