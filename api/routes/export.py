router = APIRouter()

BRAND_GREEN = "0B4B2A"
BRAND_GREEN_LIGHT = "2E7D4F"
DISCLAIMER_AR = "هذه الدراسة القانونية تم توليدها بواسطة نظام الباحث. مسؤولية التحقق من صحة المعلومات والوقائع تقع على المستخدم."
DISCLAIMER_EN = "This legal study was generated by Albaheth AI system. The user is responsible for verifying the accuracy of all information and facts."


def _get_user_from_auth(authorization: str | None, token: str | None = None) -> int | None:
    raw = token or authorization
    if not raw:
        return None
    raw = raw.replace("Bearer ", "")
    session = get_session(raw)
    return session["user_id"] if session else None


def _get_study(study_id: int, user_id: int) -> dict:
    row = query_one(
        "SELECT id, query, content, citations, created_at FROM legal_studies WHERE id = %s AND user_id = %s;",
        [study_id, user_id],
    )
    if not row:
        raise HTTPException(status_code=404, detail="الدراسة غير موجودة")
    result = dict(row)
    if isinstance(result.get("citations"), str):
        result["citations"] = json.loads(result["citations"])
    return result


def _reshape_arabic(text: str) -> str:
    """Reshape Arabic letters for connected glyph rendering (no bidi reversal)."""
    try:
        import arabic_reshaper
        reshaped = arabic_reshaper.reshape(text)
        return reshaped
    except Exception:
        return text


def _shape_for_pdf(text: str) -> str:
    """Reshape Arabic and apply bidi for PDF canvas drawing (not for Paragraph)."""
    try:
        import arabic_reshaper
        from bidi.algorithm import get_display
        reshaped = arabic_reshaper.reshape(text)
        return get_display(reshaped)
    except Exception:
        return text


def _strip_markdown(text: str) -> str:
    """Remove markdown markers from text for plain rendering."""
    text = re.sub(r'\*\*(.+?)\*\*', r'\1', text)
    text = re.sub(r'\*(.+?)\*', r'\1', text)
    text = re.sub(r'#{1,6}\s+', '', text)
    text = re.sub(r'`(.+?)`', r'\1', text)
    return text


def _is_heading(line: str) -> bool:
    return bool(re.match(r'^#{1,6}\s+', line.strip()))


def _is_numbered_heading(line: str) -> bool:
    return bool(re.match(r'^\d+\.\s+', line.strip()))


def _is_bold_line(line: str) -> bool:
    stripped = line.strip()
    return stripped.startswith('**') and stripped.endswith('**') and len(stripped) > 4


def _format_citation(i: int, cite: dict) -> str:
    """Format a citation the same way the webpage does."""
    text = (
        f"{i+1}. رقم الحكم: {cite.get('judgment_number', 'غير محدد')} — "
        f"المحكمة: {cite.get('court_type', '')} {cite.get('court_level', '')} — "
        f"المدينة: {cite.get('city', '')}"
    )
    if cite.get("case_number"):
        text += f" | رقم القضية: {cite['case_number']}/{cite.get('case_year', '')}"
    if cite.get("judgment_date_hijri"):
        text += f" | تاريخ الحكم: {cite['judgment_date_hijri']}"
    return text


